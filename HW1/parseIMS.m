function [ims,imsHSV] = parseIMS(data,labels)
% This function parses the image data for Homework 1.

% This splits out the image data into the respective red, green, and blue
% values of the RGB data.
reds = data(:,1:1024);
greens = data(:,1025:2048);
blues = data(:,2049:3072);

% Initialize the images and HSV images.
ims = cell(10,1000);
imClassCounter = ones(10,1);
imsHSV = cell(10,1000);

% For loop, Loop over each of the 10000 images, and collect the reds,
% greens, and blues into a corresponding image structure.
for i = 1:10000
    ims{labels(i)+1,imClassCounter(labels(i)+1)} = zeros(32,32,3);
    ims{labels(i)+1,imClassCounter(labels(i)+1)}(:,:,1) = [reds(i,1:32);...
        reds(i,33:64);reds(i,65:96);reds(i,97:128);reds(i,129:160);...
        reds(i,161:192);reds(i,193:224);reds(i,225:256);reds(i,257:288);...
        reds(i,289:320);reds(i,321:352);reds(i,353:384);reds(i,385:416);...
        reds(i,417:448);reds(i,449:480);reds(i,481:512);reds(i,513:544);...
        reds(i,545:576);reds(i,577:608);reds(i,609:640);reds(i,641:672);...
        reds(i,673:704);reds(i,705:736);reds(i,737:768);reds(i,769:800);...
        reds(i,801:832);reds(i,833:864);reds(i,865:896);reds(i,897:928);...
        reds(i,929:960);reds(i,961:992);reds(i,993:1024)];
    ims{labels(i)+1,imClassCounter(labels(i)+1)}(:,:,2) = ...
        [greens(i,1:32);greens(i,33:64);greens(i,65:96);...
        greens(i,97:128);greens(i,129:160);greens(i,161:192);...
        greens(i,193:224);greens(i,225:256);greens(i,257:288);...
        greens(i,289:320);greens(i,321:352);greens(i,353:384);...
        greens(i,385:416);greens(i,417:448);greens(i,449:480);...
        greens(i,481:512);greens(i,513:544);greens(i,545:576);...
        greens(i,577:608);greens(i,609:640);greens(i,641:672);...
        greens(i,673:704);greens(i,705:736);greens(i,737:768);...
        greens(i,769:800);greens(i,801:832);greens(i,833:864);...
        greens(i,865:896);greens(i,897:928);greens(i,929:960);...
        greens(i,961:992);greens(i,993:1024)];
    ims{labels(i)+1,imClassCounter(labels(i)+1)}(:,:,3) = ...
        [blues(i,1:32);blues(i,33:64);blues(i,65:96);blues(i,97:128);...
        blues(i,129:160);blues(i,161:192);blues(i,193:224);...
        blues(i,225:256);blues(i,257:288);blues(i,289:320);...
        blues(i,321:352);blues(i,353:384);blues(i,385:416);...
        blues(i,417:448);blues(i,449:480);blues(i,481:512);...
        blues(i,513:544);blues(i,545:576);blues(i,577:608);...
        blues(i,609:640);blues(i,641:672);blues(i,673:704);...
        blues(i,705:736);blues(i,737:768);blues(i,769:800);...
        blues(i,801:832);blues(i,833:864);blues(i,865:896);...
        blues(i,897:928);blues(i,929:960);blues(i,961:992);...
        blues(i,993:1024)];
    
    imClassCounter(labels(i)+1) = imClassCounter(labels(i)+1) + 1;
end

for i = 1:10
    for j = 1:1000
        % Initialize the HSV image. Convert the RGB data to HSV data.
        imsHSV{i,j} = zeros(32,32,3);
        imsHSV{i,j} = rgb2hsv(ims{i,j});
    end
end
end